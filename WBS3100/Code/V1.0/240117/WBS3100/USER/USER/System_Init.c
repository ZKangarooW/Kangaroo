#include "System_Init.h"

/*
*****************************************************************
 * 函数原型： void System_Init(void)
 * 功    能： 系统功能初始化
*****************************************************************
*/
void System_Init(void)
{	
	/**********系统初始化成功**********/
	sys.Init_ok = 0;

	/**********参数初始化**************/
	Param_Read();

	/**********ADC&DMA初始化********/  
	ADCDMA_Init();
	
    /**********LCD初始化***************/
	Lcd_Init();

	/**********编码器初始化************/
	Encoder_Init();

	/**********电机初始化**************/
	Motor_Init();

	/**********电机PID系数************/
	Motor_PID();

	/**********温度初始化**************/
	Temp_Init();

	/**********温度PID系数************/
	Temp_PID();

	/**********开机蜂鸣器响************/
	Beep_Time = 0.1;

	/**********系统初始化成功**********/
	sys.Init_ok = 1;
}
