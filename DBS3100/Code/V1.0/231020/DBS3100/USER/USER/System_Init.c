#include "System_Init.h"

/*
*****************************************************************
 * 函数原型： void System_Init(void)
 * 功    能： 系统功能初始化
*****************************************************************
*/
void System_Init(void)
{	
	/**********系统初始化成功**********/
	sys.Init_ok = 0;

	/**********LCD初始化***************/
	Lcd_Init();
	
	/**********开机蜂鸣器响************/
	Beep_Time = 0.1;

	/**********系统初始化成功**********/
	sys.Init_ok = 1;
}
