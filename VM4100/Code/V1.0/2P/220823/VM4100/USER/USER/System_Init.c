#include "System_Init.h"

/*
*****************************************************************
 * 函数原型：void System_Init(void)
 * 功    能：系统功能初始化
*****************************************************************
*/
void System_Init(void)
{
	/**********系统初始化开始**********/
	sys.Init_ok = 0;
	
	/**********LCD初始化***************/
	Lcd_Init();

	/**********编码器初始化************/
	Encoder_Init();
	
	/**********电机初始化**************/
	Motor_Init();
	
	/**********PID初始化***************/
	PID_Init();

	/**********参数初始化**************/
	Param_Read();
	
	/**********开机蜂鸣器响************/
	Beep_Time = 0.1;

	/**********系统初始化成功**********/
	sys.Init_ok = 1;
}
