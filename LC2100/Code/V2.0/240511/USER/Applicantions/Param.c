#include "Param.h"

/**********结构体**********/
struct _Save_Param_ Param;//原始数据

/**********全局变量声明******/
uint8_t Save_Param_En;

/*
*****************************************************************
 * 函数原型：void Param_Reset(void)
 * 功    能：初始化硬件中的参数
*****************************************************************
*/
void Param_Reset(void)
{
	Param.Flash_Check_Start = FLASH_CHECK_START;
	
	Param.P_Param[0] = 300;//时间
	Param.P_Param[2] = 1700;//离心率
	Param.type = 1;
	
	if(Param.type==0)
		Param.P_Param[1] = 5000;//转速
	else if(Param.type==1)
		Param.P_Param[1] = 4000;//转速
	else if(Param.type==2)
		Param.P_Param[1] = 4500;//转速
		
	Param.Flash_Check_End  = FLASH_CHECK_END;
}

/*
*****************************************************************
 * 函数原型： void Param_Save(void)
 * 功    能： 保存硬件中的参数
*****************************************************************
*/
void Param_Save(void)
{
	Flash_Write((uint8_t *)(&Param),sizeof(Param));
}

/*
*****************************************************************
 * 函数原型：void Param_Read(void)
 * 功    能：读取硬件中的参数，判断是否更新
*****************************************************************
*/
void Param_Read(void)
{
	Flash_Read((uint8_t *)(&Param),sizeof(Param));
	
	//板子从未初始化
	if(Param.Flash_Check_Start != FLASH_CHECK_START || Param.Flash_Check_End != FLASH_CHECK_END)
	{
		Param_Reset();
		Time.Set = Param.P_Param[0];//时间
		if(Speed.Unit)
			Speed.Set = Param.P_Param[2];//离心率
		else
			Speed.Set = Param.P_Param[1];//转速
		SetOK_Flag = 1;//设置
		Save_Param_En = 1;//保存
	}
	else
	{
		Time.Set = Param.P_Param[0];//时间
		if(Speed.Unit)
			Speed.Set = Param.P_Param[2];//离心率
		else
			Speed.Set = Param.P_Param[1];//转速
		SetOK_Flag = 1;//设置
	}
	
	if(!KEY2 && !KEY3)
	{
		Param.type++;
		if(Param.type > 2)
			Param.type = 0;
		Save_Param_En = 1;
		switch(Param.type)
		{
			case 0:
				Speed_MAX = 5000;
				Xg_MAX = 2600;
				if(Speed.Unit)
					Speed.Set = Xg_MAX;//离心率
				else
					Speed.Set = Speed_MAX;//转速
				break;
			case 1:
				Speed_MAX = 4000;
				Xg_MAX = 1700;
				if(Speed.Unit)
					Speed.Set = Xg_MAX;//离心率
				else
					Speed.Set = Speed_MAX;//转速
				break;
			case 2:
				Speed_MAX = 4500;
				Xg_MAX = 2150;
				if(Speed.Unit)
					Speed.Set = Xg_MAX;//离心率
				else
					Speed.Set = Speed_MAX;//转速
				break;
		}
		Param.P_Param[1] = Speed_MAX;
		Param.P_Param[2] = Xg_MAX;
	}
	switch(Param.type)
	{
		case 0:
			Speed_MAX = 5000;
			Xg_MAX = 2600;
			break;
		case 1:
			Speed_MAX = 4000;
			Xg_MAX = 1700;
			break;
		case 2:
			Speed_MAX = 4500;
			Xg_MAX = 2150;
			break;
	}
	
	//保存参数
	if(Save_Param_En)
	{
		Save_Param_En = 0;
		Param_Save();
	}
}

/*
*****************************************************************
 * 函数原型：void Param_Save_Overtime(float dT)
 * 功    能：保存标志位置1，0.5s后保存
*****************************************************************
*/
void Param_Save_Overtime(float dT)
{
	static float time;
	
	if(Save_Param_En)
	{
		time += dT;
		
		if(time >= 0.5f)
		{
			Param_Save();
			Save_Param_En = 0;
		}
	}
	else 
		time = 0;
}
